      SUBROUTINE DEBYE(WAVL,TC,KEY,RE,AI)
!CC
!CC    CALCULATES WAVENUMBER DEPENDENCE OF DIELECTRIC CONSTANT
!CC    OF WATER

!     COMMONS:
!     /RCNSTN/
!       PI       THE CONSTANT PI
!       DEG      NUMBER OF DEGREES IN ONE RADIAN.
!       BIGNUM   MAXIMUM SINGLE PRECISION NUMBER.
!       BIGEXP   MAXIMUM EXPONENTIAL ARGUMENT WITHOUT OVERFLOW.
!       RRIGHT   SMALLEST SINGLE PRECISION REAL ADDED TO 1 EXCEEDS 1.
      REAL PI,DEG,BIGNUM,BIGEXP,RRIGHT
      COMMON/RCNSTN/PI,DEG,BIGNUM,BIGEXP,RRIGHT
      T=TC+273.15
      IF(KEY.NE.0) GO TO 1
      GO TO 2
    1 EFIN=5.27137+.0216474*TC-.00131198*TC*TC
      ALPHA=-16.8129/T+.0609265
      TAU=.00033836*EXP(2513.98/T)
      SIG=12.5664E+08
      ES=78.54*(1.-.004579*(TC-25.)+.0000119*(TC-25.)**2-.000000028*    &
     &(TC-25.)**3)
      GO TO 3
    2 EFIN=3.168
      ALPHA=.00023*TC*TC+.0052*TC+.288
      T125 = 12500./(T*1.9869)
      IF(T125.LE.BIGEXP)THEN
          SIG=1.26*EXP(-T125)
      ELSE
          SIG=0.
      ENDIF
      TAU=9.990288E-05*EXP(13200./(T*1.9869))
      ES=3.168+.15*TC*TC+2.5*TC+200.
    3 C1=TAU/WAVL
!CC
!CC    TEMPORARY FIX TO CLASSICAL DEBYE EQUATION
!CC    TO HANDLE ZERO CM-1 PROBLEM
!CC
      ALPHA=0.0
      SIG=0.0
!CC
      C2=1.5708*ALPHA
      DEM=1.+2.*C1**(1.-ALPHA)*SIN(C2)+C1**(2.*(1.-ALPHA))
      E1=EFIN+(ES-EFIN)*(1.+(C1**(1.-ALPHA)*SIN(C2)))/DEM
      IF(KEY.NE.0.AND.WAVL.GE.300.) E1=87.53-0.3956*TC
      IF(KEY.NE.0 .AND. WAVL.GE.300.) E1=ES
      E2=(ES-EFIN)*C1**(1.-ALPHA)*COS(C2)/DEM+SIG*WAVL/18.8496E+10
!CC
!CC    PERMANENT FIX TO CLSSICAL DEBYE EQUATION
!CC    TO HANDLE ZERO CM-1 PROBLEM
!CC
      E1=EFIN+(ES-EFIN)/(1.0+C1**2)
!CC
      E2=((ES-EFIN)*C1)/(1.0+C1**2)
!CC
      RE=SQRT((E1+SQRT(E1*E1+E2*E2))/2.)
      AI=-E2/(2.*RE)
      RETURN
      END
