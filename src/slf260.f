      SUBROUTINE SLF260(V1C,SH2OT1)

!     LOADS SELF CONTINUUM 260K.
      IMPLICIT NONE

!     INPUT ARGUMENTS:
      REAL V1C,SH2OT1

!     COMMONS:

!     /S260DT/
!       V1S260   INITIAL SPECTRAL FREQUENCY [CM-1].
!       DVS260   SPECTRAL FREQUENCY STEP SIZE [CM-1].
!       N_S260   NUMBER OF SPECTRAL POINTS.
!       S260     SELF BROADENED H2O CONTINUUM DATA [1E-20*CM**3/MOL].
      INTEGER N_S260
      REAL V1S260,DVS260,S260
      COMMON/S260DT/V1S260,DVS260,N_S260,S260(2003)
      SAVE /S260DT/

!     DECLARE BLOCK DATA ROUTINES EXTERNAL:
      EXTERNAL SF260
      CALL SINT(V1S260,V1C,DVS260,N_S260,S260,SH2OT1)
      RETURN
      END
