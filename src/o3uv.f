     & 6.24621E+01, 6.34160E+01, 6.43622E+01/

!     INTERPOLATION FOR O3 CONTINUUM:
      I_O3UV=INT((V-V1O3UV)/DVO3UV+1.00001)
      IF(I_O3UV.LT.1 .OR. I_O3UV.GT.N_O3UV)THEN
          O3UV=0.
      ELSE
          VR=(I_O3UV-1)*DVO3UV+V1O3UV
          IF(VR.GT.V+.1 .OR. VR.LT.V-.1)THEN
              IF(I_O3UV.EQ.N_O3UV)I_O3UV=N_O3UV-1
              O3UV=(S(I_O3UV+1)-S(I_O3UV))*(V-VR)/DVO3UV+S(I_O3UV)
          ELSE
              O3UV=S(I_O3UV)
          ENDIF
      ENDIF
      RETURN
      END
